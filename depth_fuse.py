class Tfuse_Net(nn.Module):

    def __init__(self, **kwargs):
        super(Tfuse_Net, self).__init__()

        self.Tfuse = Tfuse()


        self.dim = 64

        self.deconv1 = nn.Sequential(
            nn.ConvTranspose2d(self.dim, self.dim, 4, 2, 1),  # [1, 64, 128, 128]
            nn.ReflectionPad2d((1, 0, 1, 0)),  
            nn.AvgPool2d(2, stride=1),
            nn.ReLU(inplace=True)
        )

        self.deconv2 = nn.Sequential(
            nn.ConvTranspose2d(self.dim, self.dim, 4, 2, 1),
            nn.ReflectionPad2d((1, 0, 1, 0)),
            nn.AvgPool2d(2, stride=1),
            nn.ReLU(inplace=True)
        )

        self.output1 = nn.Sequential(
            nn.Conv2d(self.dim, self.dim, kernel_size=5, stride=1, padding=2),
            nn.Conv2d(self.dim, self.dim, kernel_size=5, stride=1, padding=2),
            nn.Conv2d(self.dim, 3, kernel_size=5, stride=1, padding=2),
            nn.Tanh()
        )
        


    def forward(self, x, y):
        y1 = self.Tfuse(x,y)
        res2=y1[1]
        x2 = self.deconv1(res2)
        x4 = self.deconv2(x2)  
        x5 = self.output1(x4)  


        return x5




